export interface PixIconProps {
  name: PixIconName;
  size?: number;
  /** Override the icon's intended fill colour. Use 'currentColor' to inherit. */
  color?: string;
  style?: CSSProperties;
}

/**
 * Render a 16×16 pixel icon. Default fill comes from the icon's intended
 * palette ({@link PIX_GRIDS}); pass `color` to override.
 */
export function PixIcon(props: PixIconProps): JSX.Element {
  const def = PIX_GRIDS[props.name];
  const fill = props.color ?? def.color;
  const size = props.size ?? 16;
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 16 16"
      shapeRendering="crispEdges"
      style={{
        imageRendering: 'pixelated',
        display: 'inline-block',
        verticalAlign: 'middle',
        ...props.style,
      }}
      aria-hidden="true"
    >
      {def.grid.flatMap((row, y) =>
        [...row].map((c, x) =>
          c === '1' ? (
            <rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} fill={fill} />
          ) : null,
        ),
      )}
    </svg>
  );
}
